package variable;

public class Var7 {

    public static void main(String[] args) {
        // 정수 100 , 실수 10.5 같은 값들을 리터럴 이라고 함
        int a = 100; // 정수
        double b = 10.5; // 실수
        boolean c = true; // 불리언 true , false
        char d  = 'A'; // 문자 한 개
        String e = "Hello Java"; // 문자열을 다루기 위한 특별한 타입

        System.out.println(a); // Ctl + d , 현재 줄의 코드를 바로 아래에 복사하는 단축키
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
    }
}
